#Escreva um programa que leia um valor em metros e o exiba convertido em centímetros e milímetros

mt = int(input("Digite um valor em metros: "))
ct = mt*100
ml = mt*1000
print("{} metros \n\nVALOR CONVERTIDO PARA CENTÍMETROS E MILÍMETROS:".format(mt))
print("\n{} centímetros \n{} milímetros".format(ct, ml))




